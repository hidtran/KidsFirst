               <p>“Kids First” is a program for the benefit of children whose parents live separately. It educates the children about the divorce process. It empowers the children by providing objective information and a safe place to share their feelings. It attempts to refocus the parents from their needs to the needs of their children.</p>         <p>Recognizing the need for a program focusing on the needs of children whose parents are separated, in 1995 Attorney Klein began looking for such a program. In 1995, he learned of the “Kids Turn” program in San Francisco which dealt with families going through divorce. The coalition adopted their idea and curriculum. Sara Doudna, MFT, became the Clinical Director, expanded upon it, and “Kids First” was born.</p>
               <p>The children are grouped into four emotionally and chronologically age appropriate workshops, ages 4–6, 7–9, 10–12 and 13–17. The children’s sessions are 90 minutes in duration for seven Saturdays with the eighth Saturday running an additional hour. Each group is staffed by a volunteer licensed mental health professional (MFT, LCSW or Psychologist) and/or a masters candidate in counseling (“trainee”) or a graduate who is gaining hours of supervised experience in the licensing process (“registered intern”).</p>
               <p>The facilitators work from a curriculum that incorporates role playing, sharing and other interactive techniques to assist the children. By interacting with other children going through similar situations and being able to express their feelings (anger, hurt, guilt, fear, etc.) the children begin to work through the difficulties they are experiencing. It helps them to better cope with the separation and to express their feelings to their parents.</p>
